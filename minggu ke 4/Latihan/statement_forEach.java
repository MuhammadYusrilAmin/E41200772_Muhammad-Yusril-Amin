package latihan;
public class statement_forEach {
    public static void main(String[] args) {
        // Membuat array
        int angka[] = {3, 1, 42, 24, 12};
        
        // menggunakan perulangan For each untuk perulangan data
        for(int x : angka){
            System.out.print(x + " ");
        }
    }  
}
